package Data_types;


public class TypeOfData {
    
    public static String name= "Java";
    public static byte bt = 127;
    public static char letter= 'a';
    public static short sh = -10000; 
    public static float fl = 15000.00f;
    public static long lo = 922337203685477580l;
    public static boolean bul = false;
    public static int num = 2;
    public static double dec = 2.00;

    public static void main(String[] args) {
        System.out.println("String data type :" + name + " byte type :" + bt + " char type :" + letter + " float type :" + fl + " short type :" + sh + " long type :" + lo + " double type :" + dec + " int type " + num + " boolean type :" + bul);
    }



}
